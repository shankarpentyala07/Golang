package main
import "fmt"

func main() {
	fmt.Println("Hello world!")
}

/*
The first line package main specifies that this is a standalone executable program rather than a library.
The second line import "fmt" imports the fmt package, which provides formatting and printing functions.
The func main() is the entry point of the program.
The fmt.Println("Hello, World!") statement prints the message "Hello, World!" to the console.

*/